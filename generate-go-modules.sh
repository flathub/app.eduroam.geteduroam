#! /bin/sh

set -ex

mkdir -p go-modules && pushd go-modules

git clone --depth=1 "--branch=$1" https://github.com/geteduroam/linux-app.git
go run github.com/dennwc/flatpak-go-mod@v0.1.0 ./linux-app
rm -rf linux-app

popd

cp go-modules/modules.txt .

MODULES_START=$(grep '################# Auto generated by github.com/dennwc/flatpak-go-mod ################' app.geteduroam.geteduroam.yml -n | head -n1 | cut -d ':' -f 1)
head app.geteduroam.geteduroam.yml "-n $MODULES_START"       > app.geteduroam.geteduroam.yml.new
sed  go-modules/go.mod.yml -e "s#^#      #" | tail -n+2 >> app.geteduroam.geteduroam.yml.new
tail app.geteduroam.geteduroam.yml -n1                       >> app.geteduroam.geteduroam.yml.new
mv   app.geteduroam.geteduroam.yml.new app.geteduroam.geteduroam.yml

rm -rf go-modules
